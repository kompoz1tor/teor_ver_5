'''
4)Есть ли статистически значимые различия в росте дочерей?

Рост матерей 172, 177, 158, 170, 178,175, 164, 160, 169

Рост взрослых дочерей: 173, 175, 162, 174, 175, 168, 155, 170, 160
'''
import numpy as np
from scipy import stats

arr_1 = np.array([172, 177, 158, 170, 178, 175, 164, 160, 169])
arr_2 = np.array([173, 175, 162, 174, 175, 168, 155, 170, 160])

print(stats.ttest_rel(arr_1, arr_2))
print(f'Так как pvalue > alpha - статистических значимых различий нет')